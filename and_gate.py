def and_gate(x: bool, y: bool) -> bool:
    """
        Performs a logical AND operation on two boolean inputs.
    
        Args:
            x (bool): The first input.
            y (bool): The second input.
    
        Returns:
            bool: The result of the logical AND operation on the two inputs.
    """
    return x and y


def and_gate3(x: bool, y: bool, z: bool) -> bool:
    """
        Performs a logical AND operation on three boolean inputs.
        
        Args:
            x (bool): The first input.
            y (bool): The second input.
            z (bool): The third input.
        
        Returns:
            bool: The result of the logical AND operation on the three inputs.
    """
    return x and y and z


def and_gate4(x: bool, y: bool, z: bool, r: bool) -> bool:
    """
    Performs a logical AND operation on four boolean inputs.
    
    Args:
        x (bool): The first input.
        y (bool): The second input.
        z (bool): The third input.
        r (bool): The fourth input.
    
    Returns:
        bool: The result of the logical AND operation on the four inputs.
    """
    return x and y and z and r
