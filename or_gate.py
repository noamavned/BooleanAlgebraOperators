def or_gate(x: bool, y: bool) -> bool:
    """
    Performs a logical OR operation on two boolean inputs.
    
    Args:
        x (bool): The first input.
        y (bool): The second input.
    
    Returns:
        bool: The result of the logical OR operation on the two inputs.
    """
    return x or y


def or_gate3(x: bool, y: bool, z: bool) -> bool:
    """
    Performs a logical OR operation on three boolean inputs.
    
    Args:
        x (bool): The first input.
        y (bool): The second input.
        z (bool): The third input.
    
    Returns:
        bool: The result of the logical OR operation on the four inputs.
    """
    return x or y or z

def or_gate4(x: bool, y: bool, z: bool, r: bool) -> bool:
    """
    Performs a logical OR operation on four boolean inputs.
    
    Args:
        x (bool): The first input.
        y (bool): The second input.
        z (bool): The third input.
        r (bool): The fourth input.
    
    Returns:
        bool: The result of the logical OR operation on the four inputs.
    """
    return x or y or z or r